def get_shap_values(explainer, input_df, scaler, feature_cols):
    """Compute SHAP values for a single prediction."""
    SPECTRAL_COLS = ['NDVI', 'NDWI', 'NDBI', 'Albedo', 'MNDWI']
    
    # Scale only spectral features
    spectral_scaled = pd.DataFrame(
        scaler.transform(input_df[SPECTRAL_COLS]),
        columns=SPECTRAL_COLS
    )
    # Add city_encoded raw (unscaled)
    spectral_scaled['city_encoded'] = input_df['city_encoded'].values
    
    input_scaled = spectral_scaled[feature_cols]
    shap_vals = explainer.shap_values(input_scaled)
    if isinstance(shap_vals, list):
        shap_vals = shap_vals[0]
    # Handle both 2D (n_samples, n_features) and 1D (n_features,) returns
    if hasattr(shap_vals, 'ndim') and shap_vals.ndim == 2:
        return shap_vals[0]
    return np.atleast_1d(shap_vals)


def get_temp_class(temp):
    if temp < 35:
        return "Low", "risk-low", "temp-cool"
    elif temp < 40:
        return "Moderate", "risk-moderate", "temp-warm"
    elif temp < 45:
        return "High", "risk-high", "temp-hot"
    else:
        return "Extreme", "risk-extreme", "temp-extreme"


def compute_heat_stress(lst, air_temp):
    """
    LST-adjusted Heat Stress approximation.
    Uses air temperature as dry bulb proxy and LST as radiant
    heat load proxy. Does not use true globe temperature or
    vapor pressure — not a literal WBGT calculation.
    Thresholds based on general outdoor heat stress guidelines.
    """
    return 0.567 * air_temp + 0.393 * lst + 3.94



def get_heat_stress_class(heat_stress):
    """Classify Heat Stress Index into heat stress risk categories."""
    if heat_stress < 28:
        return "Low risk", "🟢", "#10b981"
    elif heat_stress < 32:
        return "Moderate risk", "🟡", "#eab308"
    elif heat_stress < 35:
        return "High risk", "🟠", "#f97316"
    else:
        return "Extreme risk", "🔴", "#ef4444"


