@st.cache_data
def build_city_map(city_name, _model, _scaler, _metadata):
    """Build a Folium map with model-predicted UHI heatmap for the selected city.

    Instead of hardcoded hotspots, generates a 60×60 spatial grid across the
    city bbox, predicts LST with the XGBoost model using spatially-varied
    features, and renders the normalised predictions as a HeatMap.

    Returns:
        (folium.Map, dict): The map object and a grid_summary dict with
        keys 'n_points', 'n_severe', 'lst_min', 'lst_max', 'lst_mean'.
    """
    city = CITIES[city_name]
    center = city["center"]
    bbox = city["bbox"]  # [min_lon, min_lat, max_lon, max_lat]
    feature_cols = _metadata['feature_cols']
    stats = _metadata['feature_stats']

    # ── 1. Generate 60×60 lat/lon grid across bbox ────────────────────
    grid_size = 60
    lats = np.linspace(bbox[1], bbox[3], grid_size)
    lons = np.linspace(bbox[0], bbox[2], grid_size)
    lon_grid, lat_grid = np.meshgrid(lons, lats)
    flat_lats = lat_grid.ravel()
    flat_lons = lon_grid.ravel()
    n_points = len(flat_lats)

    # ── 2. Build feature matrix with spatial variation ───────────────
    rng = np.random.RandomState(hash(city_name) % (2**31))

    # Start from mean values for all features
    grid_data = {}
    base_feature_cols = _metadata.get('base_feature_cols', feature_cols)
    city_stats = _metadata.get('city_feature_stats', {}).get(city_name, {})

    for col in base_feature_cols:
        col_stats = city_stats.get(col, stats.get(col, {}))
        grid_data[col] = np.full(n_points, col_stats.get('mean', 0.0))

    # Add city_encoded as constant column
    grid_data['city_encoded'] = np.full(
        n_points,
        _metadata.get('city_encoding', {}).get(city_name, 0)
    )

    # Initialize before conditional block
    ndvi_perturbation = None

    # Spatially vary NDVI: smooth random field ±0.15
    if 'NDVI' in grid_data:
        ndvi_noise = rng.randn(grid_size, grid_size)
        # Smooth with 5x5 box filter for spatial coherence
        ndvi_field = uniform_filter(ndvi_noise, size=5, mode='reflect')
        # Normalise to [-1, 1] then scale to ±0.15
        ndvi_range = ndvi_field.max() - ndvi_field.min()
        if ndvi_range > 0:
            ndvi_field = (ndvi_field - ndvi_field.min()) / ndvi_range * 2 - 1
        ndvi_perturbation = ndvi_field.ravel() * 0.15
        grid_data['NDVI'] = np.clip(
            grid_data['NDVI'] + ndvi_perturbation,
            stats['NDVI']['min'], stats['NDVI']['max']
        )

    # Spatially vary NDBI: inversely correlated with NDVI ±0.10
    if 'NDBI' in grid_data and ndvi_perturbation is not None:
        ndbi_perturbation = -ndvi_perturbation * (0.10 / 0.15)  # inverse correlation
        grid_data['NDBI'] = np.clip(
            grid_data['NDBI'] + ndbi_perturbation,
            stats['NDBI']['min'], stats['NDBI']['max']
        )

    # ── 3. Predict LST for every grid point ────────────────────────
    grid_df = pd.DataFrame(grid_data, columns=feature_cols)
    
    SPECTRAL_COLS = ['NDVI', 'NDWI', 'NDBI', 'Albedo', 'MNDWI']
    
    # Scale only spectral features
    spectral_scaled = pd.DataFrame(
        _scaler.transform(grid_df[SPECTRAL_COLS]),
        columns=SPECTRAL_COLS
    )
    # Add city_encoded raw (unscaled)
    spectral_scaled['city_encoded'] = grid_df['city_encoded'].values
    
    predicted_lst = _model.predict(spectral_scaled[feature_cols])

    # ── 4. Normalise to 0–1 intensity ──────────────────────────────
    lst_min, lst_max = float(predicted_lst.min()), float(predicted_lst.max())
    lst_range = lst_max - lst_min
    if lst_range > 0:
        intensity = (predicted_lst - lst_min) / lst_range
    else:
        intensity = np.full(n_points, 0.5)

    # ── 5. Build Folium map ──────────────────────────────────────
    m = folium.Map(
        location=center,
        zoom_start=city["zoom"],
        tiles=None,
        control_scale=True,
    )

    folium.TileLayer(
        tiles="https://{s}.basemaps.cartocdn.com/dark_all/{z}/{x}/{y}{r}.png",
        attr='&copy; <a href="https://carto.com/">CARTO</a>',
        name="Dark Basemap",
    ).add_to(m)

    folium.TileLayer(
        tiles="https://server.arcgisonline.com/ArcGIS/rest/services/World_Imagery/MapServer/tile/{z}/{y}/{x}",
        attr="Esri World Imagery",
        name="Satellite",
    ).add_to(m)

    # Bounding box rectangle
    folium.Rectangle(
        bounds=[[bbox[1], bbox[0]], [bbox[3], bbox[2]]],
        color="#f97316",
        weight=2,
        fill=False,
        dash_array="8",
        popup=f"{city_name} Analysis Region",
        tooltip=f"Bounding Box: {bbox[1]:.2f}°N–{bbox[3]:.2f}°N, {bbox[0]:.2f}°E–{bbox[2]:.2f}°E",
    ).add_to(m)

    # ── 6. Model-predicted HeatMap overlay ────────────────────────
    heat_data = [
        [float(flat_lats[i]), float(flat_lons[i]), float(intensity[i])]
        for i in range(n_points)
    ]
    HeatMap(
        heat_data,
        name="UHI Heatmap (Model-Predicted)",
        min_opacity=0.35,
        max_zoom=15,
        radius=18,
        blur=22,
        gradient={
            0.2: "#2563eb",
            0.4: "#06b6d4",
            0.5: "#10b981",
            0.6: "#eab308",
            0.75: "#f97316",
            0.9: "#ef4444",
            1.0: "#dc2626",
        },
    ).add_to(m)

    # ── 7. Top-5 hotspot markers from model predictions ───────────
    # Compute Heat Stress Index for hotspots using mean Air_Temp from metadata
    city_feature_stats = _metadata.get('city_feature_stats', {})
    mean_air_temp = city_feature_stats.get(city_name, {}).get('Air_Temp', {}).get('mean', stats.get('Air_Temp', {}).get('mean', 30.0))

    top5_indices = np.argsort(predicted_lst)[-5:][::-1]
    for rank, idx in enumerate(top5_indices):
        spot_lat = float(flat_lats[idx])
        spot_lon = float(flat_lons[idx])
        spot_lst = float(predicted_lst[idx])
        spot_intensity = float(intensity[idx])
        spot_heat_stress = compute_heat_stress(spot_lst, mean_air_temp)
        spot_heat_stress_label, spot_heat_stress_icon, _ = get_heat_stress_class(spot_heat_stress)
        pct = int(spot_intensity * 100)
        color = "red" if spot_intensity > 0.85 else "orange" if spot_intensity > 0.7 else "yellow"
        folium.CircleMarker(
            location=[spot_lat, spot_lon],
            radius=8,
            color=color,
            fill=True,
            fill_color=color,
            fill_opacity=0.8,
            popup=(
                f"<b>Hotspot #{rank+1}</b><br>"
                f"Predicted LST: {spot_lst:.1f}°C<br>"
                f"Heat Stress Index: {spot_heat_stress:.1f}°C {spot_heat_stress_icon} {spot_heat_stress_label}<br>"
                f"Intensity: {pct}%<br>"
                f"Lat: {spot_lat:.4f}<br>"
                f"Lon: {spot_lon:.4f}"
            ),
            tooltip=f"🔥 #{rank+1} — LST {spot_lst:.1f}°C | Heat Stress Index {spot_heat_stress:.1f}°C {spot_heat_stress_icon}",
        ).add_to(m)

    folium.LayerControl(collapsed=False).add_to(m)

    # ── 8. Grid summary for coord cards ───────────────────────────
    severe_threshold = lst_min + 0.75 * lst_range if lst_range > 0 else lst_max
    n_severe = int(np.sum(predicted_lst >= severe_threshold))
    grid_summary = {
        'n_points': n_points,
        'n_severe': n_severe,
        'lst_min': lst_min,
        'lst_max': lst_max,
        'lst_mean': float(predicted_lst.mean()),
    }

    # Return grid data for vulnerability overlay (built outside cache)
    grid_arrays = {
        'flat_lats': flat_lats.tolist(),
        'flat_lons': flat_lons.tolist(),
        'intensity': intensity.tolist(),
        'predicted_lst': predicted_lst.tolist(),
    }

    # Return HTML string for safe serialization with cache_data
    return m._repr_html_(), grid_summary, grid_arrays


