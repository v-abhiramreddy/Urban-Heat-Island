@st.cache_resource
def load_model_artifacts():
    """Load the trained model, scaler, metadata, SHAP background data, and PINN artifacts."""
    base = Path(__file__).parent
    paths = {
        "model": base / "xgb_model.pkl",
        "scaler": base / "scaler.pkl",
        "meta": base / "model_metadata.pkl",
        "xtest": base / "X_test_sample.pkl",
    }
    missing = [p.name for p in paths.values() if not p.exists()]
    if missing:
        return None, None, None, None, None, missing

    try:
        artifacts = {}
        for key, path in paths.items():
            if key == "scaler":
                scaler_path = path
                # scaler.pkl is used ONLY by the PINN comparison branch for
                # input normalization. It is NOT used in the primary XGBoost
                # prediction path (XGBoost uses raw unscaled features directly).
                # Removing it will not affect city heatmaps or LST predictions
                # but WILL break the PINN panel if enabled.
                with open(scaler_path, 'rb') as f:
                    scaler = pickle.load(f)
                artifacts[key] = scaler
            else:
                with open(path, "rb") as f:
                    artifacts[key] = pickle.load(f)
    except Exception as e:
        return None, None, None, None, None, [f"Error loading files: {e}"]

    # Load PINN artifacts (optional — not a hard requirement)
    pinn_artifacts_path = base / "pinn_artifacts.pkl"
    if pinn_artifacts_path.exists():
        try:
            with open(pinn_artifacts_path, "rb") as f:
                artifacts["pinn"] = pickle.load(f)
        except Exception:
            artifacts["pinn"] = None
    else:
        artifacts["pinn"] = None

    return artifacts["model"], artifacts["scaler"], artifacts["meta"], artifacts["xtest"], artifacts["pinn"], []


