# ── PINN Model Definition ─────────────────────────────────────────────────────
def build_pinn_model(input_dim=5):
    """Define the Physics-Informed Neural Network matching pinn_training_fixed.py.

    Architecture: 3 hidden layers (64 → 32 → 16) with ReLU, single output.
    Returns a torch.nn.Module or None if torch is unavailable.
    """
    if not HAS_TORCH:
        return None

    class UHI_PINN(nn.Module):
        def __init__(self, in_features):
            super().__init__()
            self.net = nn.Sequential(
                nn.Linear(in_features, 64),
                nn.ReLU(),
                nn.Linear(64, 32),
                nn.ReLU(),
                nn.Linear(32, 16),
                nn.ReLU(),
                nn.Linear(16, 1),
            )

        def forward(self, x):
            return self.net(x)

    model = UHI_PINN(input_dim)
    model.eval()
    return model


@st.cache_resource
def get_pinn_model(input_dim):
    """Cached PINN model instance. Returns (model, trained_flag)."""
    base = Path(__file__).parent
